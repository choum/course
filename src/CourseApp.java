    public void setCourseList() {
        //Set course list
        cList = c.getCourseList();  
        courseComboBox.setModel(new javax.swing.DefaultComboBoxModel(cList.toArray()));
    }
    
    public void setStudentList() {
        //Set student list
        sList = c.getStudentList(String.valueOf(courseComboBox.getSelectedItem()));
        for (int i = 0; i < sList.size(); i++) {
            d.addElement(sList.get(i));
        }
        studentList.setModel(d);        
    }
    public void deleteStudentList() {
        d.removeAllElements();
        sList.clear();
    }
    
