    private void deleteCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCourseButtonActionPerformed
        //Ask if person wants to delete
        int input =  JOptionPane.showConfirmDialog(null, "Are you sure you want to delete " + courseComboBox.getSelectedItem() + " and it's associated students?", "Delete course", JOptionPane.YES_NO_OPTION);
        
        //If yes, delete course
        if (input == 0) {
            //Call deleteCourse method in CourseData
            c.deleteCourse(String.valueOf(courseComboBox.getSelectedItem()));
            //Clear the cList
            cList.clear();
            //Set course list
            setCourseList();
            //Delete student list
            deleteStudentList();
            //Set student list
            setStudentList();
        }
    }//GEN-LAST:event_deleteCourseButtonActionPerformed

    private void addCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCourseButtonActionPerformed
        //If there is the same course existing
        if (cList.contains(courseTextField.getText())) {
            JOptionPane.showMessageDialog(null, "You must enter a new course into the text field!", "Existing course", JOptionPane.ERROR_MESSAGE);
            courseTextField.setText("");
        } 
        //If the Text Field is empty
        else if (courseTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "You must enter a course into the text field!", "Missing a course", JOptionPane.ERROR_MESSAGE);
        }
        
        //Add the course
        else {
            //Call addCourse method in CourseData
            c.addCourse(String.valueOf(courseTextField.getText()));
            //Update courseList
            cList.clear();
            setCourseList();
            //Move combobox index to new course
            courseComboBox.setSelectedIndex(cList.size() - 1);
            //Reset the text in the text field on the GUI
            courseTextField.setText("");
        }
    }//GEN-LAST:event_addCourseButtonActionPerformed

    private void addStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStudentButtonActionPerformed
        if (courseTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "You must enter a student name into the text field!", "Missing a student name", JOptionPane.ERROR_MESSAGE);
        } 
        else {
            //Call addStudent method from CourseData
            c.addStudent(String.valueOf(courseComboBox.getSelectedItem()), courseTextField.getText());
            //Delete students in array and list
            deleteStudentList();
            //Update students list
            setStudentList();
            //Reset text in Text Field in GUI
            courseTextField.setText("");
            
        } 
    }//GEN-LAST:event_addStudentButtonActionPerformed

    private void deleteStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteStudentButtonActionPerformed
        if (studentList.getSelectedValuesList().isEmpty()) {
            JOptionPane.showMessageDialog(null, "You must select a student name for deletion", "Did not select a student", JOptionPane.ERROR_MESSAGE);
        } 
        else {
            //Store selected values into a list
            List <String> l = studentList.getSelectedValuesList();
            //Ask if user wants to delete
            int input =  JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the following students from " + courseComboBox.getSelectedItem()  + "?\n" + studentList.getSelectedValuesList(), "Delete course", JOptionPane.YES_NO_OPTION);
            //If user wants to delete...
            if (input == 0) {
                //Store combo box item
                String s = String.valueOf(courseComboBox.getSelectedItem());
                //Call deleteStudent method in CourseData
                c.deleteStudent(l, s);
                //Delete students in array and list
                deleteStudentList();
                //Update student list
                setStudentList();
                //Reset text field
                courseTextField.setText("");
            }
        }
    }//GEN-LAST:event_deleteStudentButtonActionPerformed

